package Day02.demo04_总结this和super;

public class Pig {
    String name;
    int age;

    public Pig() {
    }

    public Pig(String name) {
        this.name = name;
    }

    public Pig(int age) {
        this.age = age;
    }

    public Pig(String name, int age) {
        this.name = name;
        this.age = age;
    }
}
